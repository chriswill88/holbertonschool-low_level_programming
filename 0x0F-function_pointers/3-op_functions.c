#include "3-calc.h"
/**
* op_add - adds two numbers
* @a: int
* @b: int
* Return: sum
*/
int op_add(int a, int b)
{
	return ((a + b));
}

/**
* op_sub - subtracts two numbers
* @a: int
* @b: int
* Return: diffrence
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - nultiplies two numbers
* @a: int
* @b: int
* Return: product
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - divides two numbers
* @a: int
* @b: int
* Return: the divide
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - apllies modulus to a and b
* @a: int
* @b: int
* Return: remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
